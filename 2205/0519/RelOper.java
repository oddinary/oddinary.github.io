public class RelOper{
	public static void main(String[] args){
		//관계연산자
		System.out.println(55 == 55); // 같은지를 물어보는 연산자 ==
		System.out.println(55 != 55); // 다른지를 물어보는 연산자 !=
		System.out.println(55 > 55); // 왼쪽이 큰 값인지 물어보는 연산자 >
		System.out.println(55 < 55); // 왼쪽이 작은 값인지 물어보는 연산자 <
		System.out.println(55 >= 55); // 왼쪽이 크거나 같은지를 물어보는 연산자 >=
		System.out.println(55 <= 55); // 왼쪽이 작거나 같은지를 물어보는 연산자 <=
	}
}