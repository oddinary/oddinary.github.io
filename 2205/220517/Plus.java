public class Plus {
    public static void main(String[] args) {
        // 메소드 (method) 호출 (call)
        System.out.println(10 + "10");
        System.out.println("20" + 20);
        System.out.println();
        System.out.println(5.123);
        System.out.println(0.1 + 0.1);

        System.out.print(1);
        System.out.print(2);
        System.out.print(3);
    }
}